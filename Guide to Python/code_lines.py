# I can create multiple 'lines' of code in one line using a semicolon.
# I see a physical line and Python translates this into logical lines.
print("s"); print("d")


# For long expressions, I can do the opposite operation with the forward
# slash (\):
c = 1 + 2 + 3\
    + 6 + 9

d = 243 - 42 *\
    8

print(c)
print(d)
